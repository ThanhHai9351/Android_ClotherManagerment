package com.example.clotherapp.MODEL;

public class Product {
    int id,star,quantity,categoryID;
    String nameProduct,description,image;
    double price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getStar() {
        return star;
    }

    public void setStar(int star) {
        this.star = star;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getCategoryID() {
        return categoryID;
    }

    public void setCategoryID(int categoryID) {
        this.categoryID = categoryID;
    }

    public String getNameProduct() {
        return nameProduct;
    }

    public void setNameProduct(String nameProduct) {
        this.nameProduct = nameProduct;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Product(int id, String image, int star, int quantity, int categoryID, String nameProduct, String description, double price) {
        this.id = id;
        this.image = image;
        this.star = star;
        this.quantity = quantity;
        this.categoryID = categoryID;
        this.nameProduct = nameProduct;
        this.description = description;
        this.price = price;
    }
}
